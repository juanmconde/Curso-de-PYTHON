
#AND (&) si tiene True y True es True sino False

resultado = True & True #devuelve True
resultado1 = True & False #devuelve False
resultado2 = False & True #devuelve False
resultado3 = False & False #devuelve False

#OR (|) Si tiene True en en algun lado es devuelve True

resultado4 = True | True #devuelve True
resultado5 = True | False #devuelve True
resultado6 = False | True #devuelve True
resultado7 = False | False #devuelve False

#NOT (not) sino es True es False sino  False es True

resultado8 = not True  #devuelve False
resultado9 = not False  #devuelve True

print(resultado9)